  <script>
    gsap.registerPlugin(ScrollTrigger);
    var tl = gsap.timeline();

    tl.from('.content', {
      opacity: 0, // at first you don't see it (0)
      y: '-30%',
      duration: 2,
      ease: Power4.easeOut // animation starts fast, gets slower to the end 
    })
    tl.from('.stagger1', {
      opacity: 0,
      y: -50,
      stagger: .3, // like a stairway --> every element in class stagger1 comes .3s later then the one before (sequencially)
      ease: Power4.easeOut,
      duration: 2
    }, "-=1.5") // offset --> 1.5s earlier 
    tl.from('.hero-design', {
      opacity: 0,
      y: 50,
      ease: Power4.easeOut,
      duration: 1
    }, "-=2")

    gsap.fromTo('.square-anim', {
      scale: 0.1, // it'll come from 10% of its value  
    }, {
      scale: 1,
      duration: 1,
      ease: Back.easeOut.config(2), // animation gets from little to bigger than normal and back to normal 
      stagger: .1
    }, "-=2.2")

    gsap.from('.transition2', {
      scrollTrigger: {
        trigger: '.transition2',
        start: "top bottom" // when the top of the element transition2 shows, it does an animation
        // until it reaches the bottom of the element 
      },
      y: 50,
      opacity: 0,
      duration: 1.2,
      stagger: .3
    })

    gsap.from('.transition3', {
      scrollTrigger: {
        trigger: '.transition3',
        start: "top center" // when the top of the element transition2 shows, it does an animation
        // until it reaches the bottom of the element 
      },
      y: 50,
      opacity: 0,
      duration: 1,
      stagger: .6
    })

    // contact
    $(document).ready(function(){
      $('.contact-animation').click(function(){
        $('.box').toggleClass('animated');
        $('.contact').toggleClass('animated'); 
        return false;
      });
    });
  </script>

